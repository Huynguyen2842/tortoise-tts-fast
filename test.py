def count_characters(text):
    return len(text)

# Example usage:
vietnamese_text = "tuy nhiên đến nay chỉ có bưu điện tỉnh thái nguyên và long an thừa nhận việc nhận tiền bồi dưỡng và đã trả lại khoản tiền này một cách tuyện đối"
character_count = count_characters(vietnamese_text)
print(f"The text contains {character_count} characters.")
